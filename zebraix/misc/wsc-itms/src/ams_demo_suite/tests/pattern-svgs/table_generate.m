function s = FormatSum(a, b, c, boldly)
  sa = "";
  sb = "";
  sc = "";
  if a >= 0
    sa = sprintf("%.3f", a);
  endif
  if b >= 0
    sb = sprintf("%.3f", b);
  endif
  if c >= 0
    sc = sprintf("%.3f", c);
  endif

  s = sprintf("%s + %s|%s", sa, sb, sc);
  ## if length(s) == 4
    ## If and only if all three strings are empty.

  if (length(sa) == 0) && (length(sb) == 0) && (length(sa) == 0)
    s = "|";
  elseif boldly
    s = sprintf("%s + %s|**%s**", sa, sb, sc);
  else
    s = sprintf("%s + %s|%s", sa, sb, sc);
  endif
endfunction



function PrintRow17(file, format, tag, row_indices, indices_printed, embolden, d0, d1, d2, d3, d4, d5)
  for k = 1:length(row_indices)
    i = row_indices(k) + 1;
    [er, ec] = size(embolden);
    if er == 0
      row_bold = false(1, 5);
    else
      row_bold = embolden(mod((i - 1), er) + 1, :);
    endif
    if length(indices_printed) >= k
      print_index = indices_printed{k};
    else
      print_index = sprintf("%d", row_indices(k));
    endif
    fprintf(file, format, tag, print_index,
            FormatSum(d0(i,4), d0(i,5), d0(i,6), row_bold(1)),
            FormatSum(d1(i,4), d1(i,5), d1(i,6), row_bold(2)),
            FormatSum(d2(i,4), d2(i,5), d2(i,6), row_bold(3)),
            FormatSum(d3(i,4), d3(i,5), d3(i,6), row_bold(4)),
            FormatSum(d4(i,4), d4(i,5), d4(i,6), row_bold(5)),
            "\n"
           );
  endfor
endfunction

